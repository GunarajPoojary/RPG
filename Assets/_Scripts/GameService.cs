using UnityEngine;

namespace RPG
{
    public class GameService : MonoBehaviour
    {
        public static GameService Instance { get; private set; }

        private void Awake()
        {
            if (Instance == null)
            {
                Instance = this;
                DontDestroyOnLoad(gameObject);
            }
            else
            {
                Destroy(gameObject);
            }
        }
    }
}